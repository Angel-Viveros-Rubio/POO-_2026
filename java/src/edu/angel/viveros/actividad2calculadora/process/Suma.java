/**
 * Clase Suma.
 * Contiene la lógica de la suma.
 */
public class Suma {

     /**
     * Realiza la suma de dos números.
     * @return regresa el resultado
     */
    public double calcular(double a, double b) {
        return a + b;
    }
}
